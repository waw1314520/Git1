package com.fire.Pojo;
public class User {
	private Integer user_id;
	private String user_name;
	private String user_identitycard;
	private String user_trainingschool;
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_identitycard() {
		return user_identitycard;
	}
	public void setUser_identitycard(String user_identitycard) {
		this.user_identitycard = user_identitycard;
	}
	public String getUser_trainingschool() {
		return user_trainingschool;
	}
	public void setUser_trainingschool(String user_trainingschool) {
		this.user_trainingschool = user_trainingschool;
	}
	
}
